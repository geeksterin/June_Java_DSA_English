package dsa_2nd_Sept_2022;

public class Calculator {

	//overloaded methods
	int add(int a, int b){
		return a + b;
	}
	
	
	float add(int a, float f) {
		return a + f;

	}
	
	
	int sub(int a, int b){
		return a - b;
	}
	
	float sub(float a, float b) {
		return a-b;
	}
		
	//add(10,20)
}
